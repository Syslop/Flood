int firstNumber = 8;
int secondNumber = 9;

if (firstNumber > secondNumber)
{
    Console.Write("max = firstNumber = ");
    Console.Write(firstNumber);
    Console.Write("; min = secondNumber = ");
    Console.Write(secondNumber);
    Console.Write(".");
} 
else
{
    Console.Write("max = secondNumber = ");
    Console.Write(secondNumber);
    Console.Write("; min = firstNumber = ");
    Console.Write(firstNumber);
    Console.Write(".");
}
